package com.example.demo.enity;


public class Activity {
    Integer number;
    Integer case_number;
    Integer type;
    String message;
    Integer updated_role;
    Integer updated_by;
    String updated;
    String updated_name;
    
    public String getUpdated_name() {
        return updated_name;
    }
    public void setUpdated_name(String updated_name) {
        this.updated_name = updated_name;
    }
    public Integer getNumber() {
        return number;
    }
    public void setNumber(Integer number) {
        this.number = number;
    }
    public Integer getCase_number() {
        return case_number;
    }
    public void setCase_number(Integer case_number) {
        this.case_number = case_number;
    }
    public Integer getType() {
        return type;
    }
    public void setType(Integer type) {
        this.type = type;
    }
    public String getMessage() {
        return message;
    }
    public void setMessage(String message) {
        this.message = message;
    }
    public Integer getUpdated_role() {
        return updated_role;
    }
    public void setUpdated_role(Integer updated_role) {
        this.updated_role = updated_role;
    }
    public Integer getUpdated_by() {
        return updated_by;
    }
    public void setUpdated_by(Integer updated_by) {
        this.updated_by = updated_by;
    }
    public String getUpdated() {
        return updated;
    }
    public void setUpdated(String updated) {
        this.updated = updated;
    }
    
    
}
