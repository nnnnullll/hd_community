package com.example.demo.enity;


public class Building {
    Integer community;
    Integer building_number;
    Integer household_amount;
    Integer height;
    public Integer getCommunity() {
        return community;
    }
    public void setCommunity(Integer community) {
        this.community = community;
    }
    public Integer getBuilding_number() {
        return building_number;
    }
    public void setBuilding_number(Integer building_number) {
        this.building_number = building_number;
    }
    public Integer getHousehold_amount() {
        return household_amount;
    }
    public void setHousehold_amount(Integer household_amount) {
        this.household_amount = household_amount;
    }
    public Integer getHeight() {
        return height;
    }
    public void setHeight(Integer height) {
        this.height = height;
    }
    
}
